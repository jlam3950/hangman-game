//Just a simple function to add two numbers
//a and b are parameters that the function receives
//When the function is called, values will be passed to a and b
function addNumber(a,b){
    let c = a +b;
    return c;
}

console.log(addNumber(5,6));//Function addNumber is called with two values 5 and 6 passed to parameters a and b
console.log(addNumber(15,23));//Function addNumber is called with two values 15 and 23 passed to parameters a and b
console.log(addNumber(115,223));//Function addNumber is called with two values 115 and 223 passed to parameters a and b
